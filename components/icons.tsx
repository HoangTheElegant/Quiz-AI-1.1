export const FileTypeIcon: React.FC<{ fileName: string, className?: string }> = ({ fileName, className }) => {
  const extension = fileName.split('.').pop()?.toLowerCase();
  
  const baseClasses = "w-6 h-6 flex-shrink-0";
  const finalClassName = `${baseClasses} ${className || ''}`;

  const DocumentShape = () => (
    <path fillRule="evenodd" d="M4.5 2.25A2.25 2.25 0 002.25 4.5v15A2.25 2.25 0 004.5 21.75h15A2.25 2.25 0 0021.75 19.5V8.25L15.75 2.25H4.5zm8.25.75V8.25h5.25L12.75 3z" clipRule="evenodd" />
  );
  
  const imageExtensions = ['jpg', 'jpeg', 'png', 'gif', 'webp', 'svg', 'bmp'];
  const audioExtensions = ['mp3', 'wav', 'ogg', 'm4a', 'flac'];

  if (extension && imageExtensions.includes(extension)) {
    return <ImageIcon className={`${finalClassName} text-purple-500`} />;
  }

  if (extension && audioExtensions.includes(extension)) {
    return <MusicalNoteIcon className={`${finalClassName} text-cyan-500`} />;
  }

  if (extension === 'docx') {
    return (
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={`${finalClassName} text-blue-500`}>
        <DocumentShape />
      </svg>
    );
  }
  if (extension === 'pdf') {
     return (
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={`${finalClassName} text-red-600`}>
        <DocumentShape />
      </svg>
    );
  }
  if (extension === 'txt') {
     return (
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={`${finalClassName} text-yellow-500`}>
        <DocumentShape />
      </svg>
    );
  }

  // Fallback icon
  return (
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={`${finalClassName} text-gray-500`}>
        <DocumentShape />
      </svg>
  );
};